    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String decision_id = request.getParameter("decision_id");
        DecisionDAO decisionDAO = new DecisionDAO();
        try {
            Decision decision = decisionDAO.getDecision(Integer.parseInt(decision_id));
            request.getSession().setAttribute("decision",decision);
            request.getRequestDispatcher("./guest/decision-overview.jsp").forward(request, response);
        } catch (NumberFormatException e) {
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String id = request.getParameter("id");
        String no = request.getParameter("no");
        String date = request.getParameter("date");

        User user = (User) request.getSession().getAttribute("user");
        if (user != null) {
            Decision decision = new Decision(Integer.parseInt(id), no, date, user.getUser_id());
            DecisionDAO decisionDAO = new DecisionDAO();
            if (decisionDAO.updateDecision(decision)) {
                response.getWriter().print("success");
                request.getSession().setAttribute("decision",decision);
            } else {
                response.getWriter().print("fail");
            }

        }else{
            response.sendRedirect("login");
        }
    }

