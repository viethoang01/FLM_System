    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String type = request.getParameter("type");
        int curriculum_id = Integer.parseInt(request.getParameter("id"));
        if (type.equals("m")) {
            // add nhieu subject
            List<Plo> importPlo = (List<Plo>) request.getSession().getAttribute("listPlo");
            if (importPlo.size() > 0) {
                for (Plo plo : importPlo) {
                    new PloDAO().addPlo(plo);
                }
            }

        }
        response.sendRedirect("plo-list?curriculum_id="+curriculum_id);
    }

